   @MAGV CHAR(3),
   @HOTEN NVARCHAR(15),
   @PHAI NVARCHAR(3),
   @NGSINH DATE,
   @DIACHI NVARCHAR(36),
   @GVQLCM CHAR(3),
   @MABM VARCHAR(4),
   @KQ INT OUT
AS
   IF @MAGV IN (SELECT MAGV FROM GIAOVIEN)
   BEGIN
      SET @KQ = 1
	  RETURN
   END

   IF @MABM NOT IN (SELECT MABM FROM BOMON) OR
   (SELECT COUNT(MAGV) FROM GIAOVIEN WHERE @MABM = MABM) > 3
   BEGIN
      SET @KQ = 1
	  RETURN
   END

   IF @GVQLCM NOT IN (SELECT @GVQLCM FROM GIAOVIEN)  
      SET @GVQLCM = (SELECT TRUONGBM FROM BOMON WHERE @MABM = MABM)

   IF @HOTEN IS NULL 
   BEGIN
      SET @KQ = 1
	  RETURN
   END

