(
    @MAGV CHAR(3),
	@MADT CHAR(3)
)
RETURNS INT
AS 
BEGIN
    IF @MAGV NOT IN (SELECT MAGV FROM THAMGIADT WHERE @MADT = MADT)
	    RETURN -1
	RETURN (SELECT COUNT(*)
	        FROM THAMGIADT TG
			WHERE TG.KETQUA IS NULL AND @MADT = TG.MADT AND @MAGV = TG.MAGV)
END

